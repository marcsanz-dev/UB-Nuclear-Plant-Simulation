    private void jFileChooser1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jFileChooser1ActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = (JFileChooser) evt.getSource();
        String command = evt.getActionCommand();
        
        if (JFileChooser.APPROVE_SELECTION.equals(command)) {
            try {
                adaptador.guardaDades(chooser.getSelectedFile().getAbsolutePath());
                JOptionPane.showMessageDialog(null, "Les dades s'han guardat correctament");
                this.dispose();
            } catch (CentralUBException e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error: ", JOptionPane.ERROR_MESSAGE);
            }
        } else if (JFileChooser.CANCEL_SELECTION.equals(command)) {
            this.dispose();
        }
    }//GEN-LAST:event_jFileChooser1ActionPerformed


